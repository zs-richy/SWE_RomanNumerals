package game;

/**
 * Enum class that declares game progresses.
 */
public enum Progress {
    /**
     * Represents that the game is still in progress.
     */
    PLAYING,

    /**
     * Represents that the game has ended and the player has won.
     */
    WON,

    /**
     * Represents that the game has ended and the player has lost.
     */
    LOST;
}
